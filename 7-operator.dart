void main() {
  // Arithmetic
  int a = 10;
  int b = 20;

  print(a + b);
  print(a - b);
  print(a * b);
  print(a / b);
  print(a % b);

  // Equal
  print(a == b);
  print(a < b);
  print(a <= b);
  print(a > b);
  print(a >= b);
  print(a != b);

  // Logical
  bool x = false;
  bool y = false;
  print(x);
  print(x || y);
}
